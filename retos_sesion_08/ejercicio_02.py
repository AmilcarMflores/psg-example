abecedario_incompleto = tuple("abcdefghij")

print(abecedario_incompleto)

# Print the first element
print(abecedario_incompleto[0])

# Print the last element
print(abecedario_incompleto[-1])

# Print a slice from index 3 to 5
print(abecedario_incompleto[3:6])

# Print a slice from index 5 to 9 with a step of 3
print(abecedario_incompleto[5:10:3])

# Print a slice from index 9 to 0 with a step of -2
print(abecedario_incompleto[9::-2])