print("Tutorial: Cómo lavar los platos")

print("\nPaso 1: Preparar todo lo que vas a necesitar.")
print("- Esponja (si podés, una con lado suave y lado verde abrasivo)")
print("- Detergente lavavajillas")
print("- Guantes (opcional, pero ayuda si el agua está muy fría)")
print("- Escurridor de platos o una toalla limpia para secar")

print("\nPaso 2: Sacar los restos de comida de los platos.")
print("- Tirá los restos en la basura.")
print("- No metas platos con comida directamente al agua, queda todo sucio y peor.")

print("\nPaso 3: Llenar la pileta con agua tibia y un chorrito de detergente.")
print("- El agua tibia ayuda a sacar la grasa.")

print("\nPaso 4: Empezá por lo que menos se ensucia:")
print("- Primero vasos y tazas")
print("- Después cubiertos")
print("- Luego platos y recipientes")
print("- Al final ollas, sartenes y cosas muy sucias")

print("\nPaso 5: Usá el lado suave para lo delicado (vasos, platos) y el abrasivo para ollas.")
print("- Si algo tiene grasa pegada, dejalo remojando un rato.")

print("\nPaso 6: Enjuagá bien con agua limpia.")
print("- Que no queden restos de jabón.")

print("\nPaso 7: Secar o dejar escurrir.")
print("- Podés dejar que se sequen al aire en el escurridor.")

print("\n¡Y listo! Platos limpios.")